package study;

public class Calculator {
    public int plus(int input1, int input2) {
        return input1 + input2;
    }

    public int minus(int input1, int input2) {
        return input1 - input2;
    }

    public int multiply(int input1, int input2) {
        return input1 * input2;
    }

    public int divide(int input1, int input2) {
        return input1 / input2;
    }
}
